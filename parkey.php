      <div class="title2"><span class="letter">Q: </span>How can we connect city commuters with unused condo building parking spots?</div><br>

      <div class="title2"><span class="letter">A: </span>Parkey</div><div class="deck">a mobile app</div>Parkey is a revolutionary new way to find parking spots in the city. Using a mobile app, you can gain access to available spots in underground condo parking garages for a reasonable price. Also, you can rest easy knowing that your car is being kept under surveillance by condo security for your entire stay.</p>
