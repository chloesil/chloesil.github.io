      <div class="title2"><span class="letter">Q: </span>How can we use storytelling to connect with people we have never met?</div><br>

      <div class="title2"><span class="letter">A: </span>Hear & Now</div><div class="deck">a mobile app</div>Travelling is a way to make our big world smaller. Many people see travel as a rite of passage, a way of broadening one’s horizons and learning more about the external world as well as the internal world (one’s inner self). While travel enables us to grow in ways that are incredibly meaningful and empowering to one’s spirit, the actual planning and funding of one’s travels can be hard to come by. Especially for young people who have the most freedom and energy needed for travel, there are no available funds or experience of how to conduct oneself in a new environment. These hurdles make travel inaccessible at a time in our lives when we are most easily suited to it.</p>

      <p><span class="bold">The goal of the app is to speed up the learning curve of being in a new place, all held within an affordable environment. Instead of finding and paying an expensive tour guide who may not be able to customize the trip to your interests, all you need is the app and access to the internet. The user-created content is both economically friendly and inspirational because it comes from the mouths of the people who know what they are talking about.</span></p>
