      <div class="title2"><span class="letter">Q: </span>How can we incorporate common materials into the communication process?</div><br>

      <div class="title2"><span class="letter">A: </span>Flora & Fauna</div><div class="deck">an experiment in material design</div>I have always had a special place in my heart for material and object design. I like to work with my hands whenever possible, and thought it might be interesting to use plantlife as a tool through which to explore a theme.</p>
      <p>I began with the theme of flora and fauna, and how they interact with each other in the cycle of life. Animals eat plants to sustain life, and in turn nourish the soil when they die.</p>
      <p><span class="bold">The goal of the piece is to imagine ways of using materials in unexpected ways in order to convey a particular theme or message. I also used processes that I had never used before such as glass etching, laser cutting and engraving, and even growing plants (for the purposes of design, at least).</span></p></div>

