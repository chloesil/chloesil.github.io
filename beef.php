            What's Your Beef
          </div>
          <div class="deck">a digital and print brochure</div>
          This project consists of a printed brochure at 22 x 34", and a completely immersive digital publication for iPad made with Digital Publishing Suite in Adobe Indesign.
        </p>
        <p>The raising of cattle for beef consumption is currently one of the main sources of greenhouse gases in the world, and a large contributor to the crisis of global warming. This publication takes the viewer through the basic facts they need in order to understand the state of the problem as it stands, as well as provides the viewer with a guide outlining ways to consume beef products in a more ecologically responsible way.</p>
        <p><span class="bold">The goal of the project is to create awareness about a world issue through two contrasting forms of media. The same content is used across both platforms, but the design approach is inherently different.</span></p>
