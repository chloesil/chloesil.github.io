      <p>Many people do not like to be confronted with hard-to-solve world issues, and especially not issues which ask them to change their lifestyle. Keeping this in mind, I designed this publication to be completely immersive and interesting for the viewer. In the digital version, every page contains interactions that further explain the information and keep the viewer enticed at the same time. <span class="bold">The publication aims to arm the viewer with knowledge without preaching or ordering the viewer to take action.</span></p></div>
      
